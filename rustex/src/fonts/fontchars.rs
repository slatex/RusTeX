macro_rules! table {
    ($name:expr,$o:ident,$(($s:tt,$iname:ident,$($para:expr),*)),* ;$rest:expr) => (
        match &$name.to_string() {
            $(s if s==$s => Some($o.insert(Arc::new(FontTable {
            params:vec!($($para),*),
            table:&$iname,
            name:$s.into()
        })).clone())),*,
        _ => $rest
        }
    )
}
